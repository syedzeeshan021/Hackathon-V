def truncate_to_words(text: str, max_words: int) -> str:
    """Truncate text to maximum word count."""
    words = text.split()
    if len(words) <= max_words:
        return text

    truncated = " ".join(words[:max_words])
    # Try to end at sentence boundary
    last_period = truncated.rfind(".")
    if last_period > max_words * 3:  # Only if we don't lose too much
        return truncated[:last_period + 1]

    return truncated.rstrip() + "..."


def truncate_to_chars(text: str, max_chars: int, break_on_word: bool = True) -> str:
    """Truncate text to maximum character count."""
    if len(text) <= max_chars:
        return text

    if break_on_word:
        # Find last word boundary
        break_point = text.rfind(" ", 0, max_chars)
        if break_point == -1:
            break_point = max_chars
        return text[:break_point].rstrip() + "..."
    else:
        return text[:max_chars - 3] + "..."


def count_words(text: str) -> int:
    """Count words in text."""
    return len(text.split())


def estimate_reading_time(text: str, words_per_minute: int = 200) -> float:
    """Estimate reading time in seconds."""
    words = count_words(text)
    return (words / words_per_minute) * 60
